function x_hat = lmmse_detector(y, H, noise_var)
% =========================================================================
% LMMSE检测器 - 线性最小均方误差检测
% =========================================================================
% 输入:
%   y         - 接收信号 [N × 1]
%   H         - 信道矩阵 [N × N]
%   noise_var - 噪声方差 (标量)
%
% 输出:
%   x_hat     - 估计符号 [N × 1]
%
% 算法:
%   x_hat = (H^H · H + σ² · I)^{-1} · H^H · y
% =========================================================================

    N = size(H, 1);
    
    % 计算 H^H
    H_H = H';
    
    % 计算 H^H · H + σ² · I
    HH_reg = H_H * H + noise_var * eye(N);
    
    % 计算 H^H · y
    Hy = H_H * y;
    
    % 求解线性方程组 (比直接求逆更稳定)
    x_hat = HH_reg \ Hy;
end
